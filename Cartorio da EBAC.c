#include <stdio.h> 	//biblioteca de comunicação com o usuário
#include <stdlib.h> //biblioteca de alocação de espaço em memória
#include <locale.h> //biblioteca de alocação de texto por região
#include <string.h> //biblioteca responsável por cuidar das strings


int registro()
{
	
	//início das variáveis
	char arquivo[40];
	char cpf[40];
	char nome[40];
	char sobrenome[40];
	char cargo[40];
	//final das variáveis
	
	printf("Digite o CPF a ser cadastrado:  ");
	scanf("%s", cpf); //%s refere-se ao scan da string 
	
	strcpy(arquivo, cpf);	//Responsável por copiar os valores das strings
	
	FILE *file; //cria o arquivo
	file = fopen(arquivo, "w"); //o W cria o arquivo e escreve
	fprintf(file, "CPF: ");
	fprintf(file,cpf);  //salva o valor da variável
	fclose(file);   //fecha o arquivo
	
	file = fopen(arquivo, "a"); //o A apenas atualiza as informações do arquivo
	fclose(file);
	
	printf("Digite o nome a ser cadastrado:  ");
	scanf("%s", nome);
	
	file = fopen(arquivo, "a");
	fprintf(file, "  Nome: ");
	fprintf(file,nome); //função fprintf insere os dados dentro do arquivo
	fclose(file);
	
	file = fopen(arquivo, "a");
	fclose(file);
	
	printf("Digite o sobrenome a ser cadastrado:  ");
	scanf("%s", sobrenome);
	
	file = fopen(arquivo, "a");
	fprintf(file, "  Sobrenome: ");
	fprintf(file, sobrenome);
	fclose(file);
	
	file = fopen(arquivo, "a");
	fclose(file);
	
	printf("Digite o cargo a ser cadastrado:  ");
	scanf("%s", cargo);
	
	file = fopen(arquivo, "a");
	fprintf(file, "  Cargo: ");
	fprintf(file, cargo);
	fclose(file);
	
	system("pause");
	
	
}


int consulta()
{
	setlocale(LC_ALL, "Portuguese");

	char cpf[40];
	char conteudo[200];
	
	printf("Digite o CPF a ser consultado: ");
	scanf("%s", cpf);
	
	FILE *file;
	file = fopen(cpf,"r");
	
	if(file == NULL)
	{
		printf("Não foi possível localizar este arquivo! \n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEssas são as informações do usuário: ");
		printf("%s", conteudo);
		printf("\n\n");
		
	}
	
		system("pause");

}


int deletar()
{
	char cpf[40];
	
	printf("Digite o CPF do usuário a ser deletado: ");
	scanf("%s", cpf);
	
	remove(cpf);
	
	FILE *file;
	file = fopen(cpf, "r");
		
	if(file == NULL)
	{
		printf("Usuário não encontrado no sistema!\n");
	}
		system("pause");
	
}

 
int main()													
{
	int opcao=0;	//Definindo variáveis
	int laco=1;
	
	for(laco=1;laco=1;)
	{
		
		setlocale(LC_ALL, "Portuguese");	//Definindo a linguagem
	
		system("cls");	
	
		printf("### Cartório da EBAC ### \n\n");    //Início do menu
		printf("Escolha a opção desejada do menu: \n\n");
		printf("\t1 - Registrar nomes\n");
		printf("\t2 - Consultar nomes\n");
		printf("\t3 - Deletar nomes\n\n\n");   //Fim do menu
		printf("Opção: ");
	
	
		scanf("%d", &opcao);		//Armazenando a escolha do usuário
	
		system("cls"); //Responsável por limpar a tela
		
		switch(opcao) //inicio da seleção do menu
		{
			case 1:
			registro(); //chamada de função
			break;
			
			case 2:
			consulta();
			break;
			
			case 3:
			deletar();
			break;
						
			default:
			printf("Opção inválida!\n");
			system("pause");
			break;	//fim da seleção			
		}
	}
}
